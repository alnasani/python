# Learn Python in Arabic #74 - الاتحاد و المشترك مع set union and intersection with set Pyth
company1 = [ 'halim','max','rahim']
company2 = ['rami','ola','muster','halim']
setcompyny1 = set(company1)
setcompyny2 = set(company2)
print(setcompyny1)
print(setcompyny2)

print(setcompyny1.union(company2))
print(setcompyny1.intersection(company2))
